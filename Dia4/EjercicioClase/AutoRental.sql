-- Creacion de tablas

CREATE TABLE sucursal(
    id INT AUTO_INCREMENT PRIMARY KEY,
    ciudad VARCHAR(100) NOT NULL,
    direccion VARCHAR(200) NOT NULL,
    telefono_fijo VARCHAR(20),
    celular VARCHAR(20),
    correo_electronico VARCHAR(100)
);

CREATE TABLE empleado(
    id INT AUTO_INCREMENT PRIMARY KEY,
    sucursal_id INT,
    FOREIGN KEY (sucursal_id) REFERENCES sucursal(id),
    cedula VARCHAR(20) UNIQUE NOT NULL,
    nombre1 VARCHAR(100) NOT NULL,
    nombre2 VARCHAR(100),
    apellido1 VARCHAR(100) NOT NULL,
    apellido2 VARCHAR(100),
    direccion VARCHAR(200) NOT NULL,
    ciudad_residencia VARCHAR(100) NOT NULL,
    celular VARCHAR(20),
    correo_electronico VARCHAR(100) NOT NULL
);

CREATE TABLE cliente (
    id INT AUTO_INCREMENT PRIMARY KEY,
    cedula VARCHAR(20) UNIQUE NOT NULL,
    nombre1 VARCHAR(100) NOT NULL,
    nombre2 VARCHAR(100),
    apellido1 VARCHAR(100) NOT NULL,
    apellido2 VARCHAR(100),
    direccion VARCHAR(200) NOT NULL,
    ciudad_residencia VARCHAR(100) NOT NULL,
    celular VARCHAR(20),
    correo_electronico VARCHAR(100) NOT NULL
);

CREATE TABLE vehiculo(
    id INT AUTO_INCREMENT PRIMARY KEY,
    tipo ENUM('sedán', 'compacto', 'camioneta platón', 'camioneta lujo', 'deportivo'),
    placa VARCHAR(20) UNIQUE NOT NULL,
    referencia VARCHAR(100),
    modelo INT NOT NULL,
    puertas INT NOT NULL,
    capacidad INT NOT NULL,
    sunroof BOOLEAN,
    motor VARCHAR(50),
    color VARCHAR(30)
);

CREATE TABLE alquileres(
    id INT AUTO_INCREMENT PRIMARY KEY,
    vehiculo_id INT NOT NULL, 
    FOREIGN KEY (vehiculo_id) REFERENCES vehiculo(id),
    cliente_id INT NOT NULL, 
    FOREIGN KEY (cliente_id) REFERENCES cliente(id),
    empleado_id INT NOT NULL, 
    FOREIGN KEY (empleado_id) REFERENCES empleado(id),
    sucursal_salida_id INT NOT NULL,
    FOREIGN KEY (sucursal_salida_id) REFERENCES sucursal(id),
    sucursal_llegada_id INT NOT NULL,
    FOREIGN KEY (sucursal_llegada_id) REFERENCES sucursal(id),
    fecha_salida DATE NOT NULL,
    fecha_llegada DATE NOT NULL,
    fecha_esperada_llegada DATE NOT NULL,
    valor_alquiler_semana DECIMAL(10, 2) NOT NULL,
    valor_alquiler_dia DECIMAL(10, 2) NOT NULL,
    porcentaje_descuento DECIMAL(5, 2),
    valor_cotizado DECIMAL(10, 2) NOT NULL,
    valor_pagado DECIMAL(10, 2)
);

