    Foreign Key (empleado_id) REFERENCES empleado(id),
    sucursal_salida_id INT NOT NULL,
    Foreign Key (sucursal_salida_id) REFERENCES sucursal(id),
    sucursal_llegada_id INT NOT NULL,
